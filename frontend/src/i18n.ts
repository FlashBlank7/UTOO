const categoryKeys: Record<string, string> = {
  全部: 'categoryAll',
  公告: 'categoryNotice',
  课程: 'categoryCourse',
  研究室: 'categoryLab',
  生活: 'categoryLife',
  租房: 'categoryHousing',
  就职: 'categoryCareer',
  闲聊: 'categoryChat'
}

function initialLocale(): Locale {
  const saved = window.localStorage.getItem(storageKey)
  if (saved === 'zh' || saved === 'en' || saved === 'ja') return saved
  const language = window.navigator.language.toLowerCase()
  if (language.startsWith('ja')) return 'ja'
  if (language.startsWith('en')) return 'en'
  return 'zh'
}

const locale = ref<Locale>(initialLocale())
export const currentLocale = computed(() => locale.value)

export function setLocale(next: Locale) {
  locale.value = next
  window.localStorage.setItem(storageKey, next)
}

export function useI18n() {
  function t(key: string, params: Record<string, string | number> = {}) {
    const template = messages[locale.value][key] ?? messages.zh[key] ?? key
    return Object.entries(params).reduce((text, [name, value]) => text.split(`{${name}}`).join(String(value)), template)
  }

  function categoryLabel(category: string | null | undefined) {
    if (!category) return ''
    const key = categoryKeys[category] ?? category
    return t(key)
  }

  function visibilityLabel(visibility: string) {
    if (visibility === 'hidden') return t('visibilityHidden')
    if (visibility === 'deleted') return t('visibilityDeleted')
    return t('visibilityNormal')
  }

  function sourceLabel(source: string) {
    return source === 'agent' ? t('sourceAgent') : t('sourceUser')
  }

  function formatDate(iso: string) {
    return new Date(iso).toLocaleString(localeMeta[locale.value].date, {
      month: 'numeric',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    })
  }

  return { locale, currentLocale, localeOptions, setLocale, t, categoryLabel, visibilityLabel, sourceLabel, formatDate }
}

function setMeta(name: string, content: string, property = false) {
  const selector = property ? `meta[property="${name}"]` : `meta[name="${name}"]`
  const element = document.querySelector<HTMLMetaElement>(selector)
  if (element) element.content = content
}

function syncDocumentLocale(next: Locale) {
  const meta = localeMeta[next]
  document.documentElement.lang = meta.html
  document.title = meta.title
  setMeta('description', meta.description)
  setMeta('og:title', meta.title, true)
  setMeta('og:description', meta.description, true)
  setMeta('twitter:title', meta.title)
  setMeta('twitter:description', meta.description)
}

watch(locale, syncDocumentLocale, { immediate: true })
